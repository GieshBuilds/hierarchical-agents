"""ClaudeCodeProfileAdapter — map ProfileRegistry profiles to CLAUDE.md files.

Claude Code reads a CLAUDE.md file at session start to understand the project
context, team structure, and agent identity. This adapter generates rich
CLAUDE.md files from hierarchy profile data, making each profile "aware" of:

1. Its own identity and role (from ProfileRegistry + SOUL.md)
2. Its place in the org chart (parent, direct reports, department)
3. The IPC tools it should use for inter-agent communication
4. Recent scoped memory entries for context continuity

.claude/ directory layout
--------------------------
Each profile gets its own project directory::

    ~/.claude/projects/
    └── <profile_name>/
        ├── CLAUDE.md          ← generated by this adapter
        └── .context/          ← optional extra context files

Stdlib only — no external dependencies.
"""

from __future__ import annotations

from pathlib import Path
from typing import Any, Dict, List, Optional

from core.registry.profile_registry import ProfileRegistry
from integrations.claude_code.config import ClaudeCodeConfig


class ClaudeCodeProfileAdapter:
    """Generates Claude Code CLAUDE.md context files from ProfileRegistry data.

    Parameters
    ----------
    registry : ProfileRegistry
        The hierarchy profile registry.
    config : ClaudeCodeConfig
        Claude Code integration configuration.
    """

    def __init__(self, registry: ProfileRegistry, config: ClaudeCodeConfig) -> None:
        self._registry = registry
        self._config = config

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    def generate_claude_md(
        self,
        profile_name: str,
        *,
        memory_entries: Optional[List[Any]] = None,
        pending_messages: Optional[List[Any]] = None,
    ) -> str:
        """Generate a CLAUDE.md string for a profile.

        Assembles the file from:
        1. Profile identity block (from ProfileRegistry)
        2. SOUL.md content (if present in profiles_dir)
        3. Org chart context (parent, direct reports)
        4. IPC tools quick reference
        5. Scoped memory entries (optional)
        6. Pending IPC messages (optional)

        Parameters
        ----------
        profile_name : str
            Name of the profile to generate context for.
        memory_entries : list, optional
            Pre-loaded memory entries to embed. If None and
            ``config.include_memory_in_claude_md`` is True, no memory
            is embedded (caller is responsible for loading memory).
        pending_messages : list, optional
            Pre-loaded IPC messages to embed.

        Returns
        -------
        str
            Full CLAUDE.md content as a string.
        """
        sections: List[str] = []

        # 1. Identity block
        sections.append(self._build_identity_section(profile_name))

        # 2. SOUL.md content
        soul_content = self._read_soul_md(profile_name)
        if soul_content:
            sections.append(
                f"## Your Identity (SOUL.md)\n\n{soul_content}"
            )

        # 3. Org chart context
        sections.append(self._build_org_context_section(profile_name))

        # 4. IPC tools quick reference
        sections.append(self._build_tools_section())

        # 5. Scoped memory
        if memory_entries and self._config.include_memory_in_claude_md:
            sections.append(self._build_memory_section(memory_entries))

        # 6. Pending IPC messages
        if pending_messages and self._config.include_pending_messages:
            sections.append(self._build_messages_section(pending_messages))

        return "\n\n---\n\n".join(sections) + "\n"

    def write_claude_md(
        self,
        profile_name: str,
        *,
        memory_entries: Optional[List[Any]] = None,
        pending_messages: Optional[List[Any]] = None,
    ) -> Path:
        """Generate and write CLAUDE.md to the profile's project directory.

        Creates the directory if it doesn't exist.

        Parameters
        ----------
        profile_name : str
            Name of the profile.
        memory_entries : list, optional
            Memory entries to embed.
        pending_messages : list, optional
            IPC messages to embed.

        Returns
        -------
        Path
            Path to the written CLAUDE.md file.
        """
        content = self.generate_claude_md(
            profile_name,
            memory_entries=memory_entries,
            pending_messages=pending_messages,
        )

        project_dir = self.get_project_dir(profile_name)
        project_dir.mkdir(parents=True, exist_ok=True)

        claude_md_path = project_dir / "CLAUDE.md"
        claude_md_path.write_text(content, encoding="utf-8")
        return claude_md_path

    def get_project_dir(self, profile_name: str) -> Path:
        """Return the Claude Code project directory for a profile.

        Parameters
        ----------
        profile_name : str
            The profile name.

        Returns
        -------
        Path
            ``<projects_dir>/<profile_name>/``
        """
        return self._config.projects_dir / profile_name

    def sync_all_profiles(
        self,
        *,
        memory_stores: Optional[Dict[str, Any]] = None,
    ) -> Dict[str, Path]:
        """Generate CLAUDE.md for every registered profile.

        Parameters
        ----------
        memory_stores : dict, optional
            Mapping of profile_name → MemoryStore. When provided,
            entries are embedded in each profile's CLAUDE.md.

        Returns
        -------
        dict
            Mapping of profile_name → Path to written CLAUDE.md.
        """
        result: Dict[str, Path] = {}
        profiles = self._registry.list_profiles()

        for profile in profiles:
            entries = None
            if memory_stores and profile.profile_name in memory_stores:
                store = memory_stores[profile.profile_name]
                if store is not None:
                    try:
                        entries = store.list_entries(
                            limit=self._config.max_memory_entries
                        )
                    except Exception:
                        entries = None

            path = self.write_claude_md(
                profile.profile_name,
                memory_entries=entries,
            )
            result[profile.profile_name] = path

        return result

    # ------------------------------------------------------------------
    # Section builders
    # ------------------------------------------------------------------

    def _build_identity_section(self, profile_name: str) -> str:
        """Build the identity/role header section."""
        try:
            profile = self._registry.get_profile(profile_name)
            role_label = profile.role.replace("_", " ").title()
            parent_info = (
                f"  - **Reports to**: `{profile.parent_profile}`\n"
                if profile.parent_profile
                else ""
            )
            dept_info = (
                f"  - **Department**: {profile.department}\n"
                if profile.department
                else ""
            )
            return (
                f"# {profile.display_name or profile_name}\n\n"
                f"**Hierarchy Profile**: `{profile_name}`  \n"
                f"**Role**: {role_label}  \n"
                f"**Status**: {profile.status}\n\n"
                f"## Profile Details\n\n"
                f"{parent_info}"
                f"{dept_info}"
            ).rstrip()
        except Exception as e:
            return (
                f"# {profile_name}\n\n"
                f"*Profile not found in registry: {e}*"
            )

    def _build_org_context_section(self, profile_name: str) -> str:
        """Build the organizational context section."""
        lines = ["## Organizational Context"]

        try:
            profile = self._registry.get_profile(profile_name)

            if profile.parent_profile:
                try:
                    parent = self._registry.get_profile(profile.parent_profile)
                    lines.append(
                        f"\n**You report to**: `{profile.parent_profile}` "
                        f"({parent.role.replace('_', ' ').title()})"
                    )
                except Exception:
                    lines.append(f"\n**You report to**: `{profile.parent_profile}`")

            direct_reports = self._registry.list_reports(profile_name)
            if direct_reports:
                lines.append("\n**Your direct reports**:")
                for report in direct_reports:
                    role_label = report.role.replace("_", " ").title()
                    lines.append(f"  - `{report.profile_name}` ({role_label})")
            else:
                lines.append("\n*You have no direct reports.*")

        except Exception as e:
            lines.append(f"\n*Could not load org context: {e}*")

        return "\n".join(lines)

    def _build_tools_section(self) -> str:
        """Build the IPC tools quick reference section."""
        return (
            "## Available Hierarchy Tools\n\n"
            "You have access to the following tools for inter-agent communication:\n\n"
            "| Tool | Purpose |\n"
            "|------|---------|\n"
            "| `send_to_profile` | Send a message to another profile via IPC. "
            "Set `wait_for_response=true` for synchronous responses. |\n"
            "| `check_inbox` | Poll your pending IPC messages. |\n"
            "| `org_chart` | Display the full organizational hierarchy. |\n"
            "| `profile_status` | Get memory stats, worker counts, and pending messages for a profile. |\n"
            "| `spawn_tracked_worker` | Spawn a worker subagent with persistent tracking. |\n"
            "| `get_project_status` | Get running/completed workers for a project manager. |\n\n"
            "### Communication Protocol\n\n"
            "1. **Check your inbox first** — always call `check_inbox` at the start of a session.\n"
            "2. **Send responses** — after completing a task, `send_to_profile` your result to the requester.\n"
            "3. **Escalate blockers** — use `priority='urgent'` when blocked and need immediate attention.\n"
            "4. **Delegate work** — use `spawn_tracked_worker` for subtasks you need to parallelize."
        )

    def _build_memory_section(self, entries: List[Any]) -> str:
        """Build the scoped memory section."""
        if not entries:
            return "## Scoped Memory\n\n*No memory entries.*"

        lines = [
            f"## Scoped Memory ({len(entries)} entries)\n",
            "Recent context from your persistent memory:\n",
        ]

        limit = self._config.max_memory_entries
        for entry in entries[:limit]:
            preview = entry.content[:300]
            if len(entry.content) > 300:
                preview += "..."

            tier = getattr(entry.tier, "value", str(entry.tier))
            entry_type = getattr(entry.entry_type, "value", str(entry.entry_type))
            lines.append(f"**[{tier}] {entry_type}**: {preview}")

        if len(entries) > limit:
            lines.append(f"*... and {len(entries) - limit} more entries.*")

        return "\n".join(lines)

    def _build_messages_section(self, messages: List[Any]) -> str:
        """Build the pending IPC messages section."""
        if not messages:
            return "## Pending Messages\n\n*No pending messages.*"

        lines = [
            f"## Pending IPC Messages ({len(messages)})\n",
            "Messages waiting in your inbox:\n",
        ]

        limit = self._config.max_pending_messages
        for msg in messages[:limit]:
            priority = getattr(msg.priority, "value", str(msg.priority))
            msg_type = getattr(msg.message_type, "value", str(msg.message_type))
            payload_preview = str(msg.payload)[:200]
            lines.append(
                f"- **[{priority}]** From `{msg.from_profile}` "
                f"({msg_type}): {payload_preview}"
            )

        if len(messages) > limit:
            lines.append(f"*... and {len(messages) - limit} more messages.*")

        return "\n".join(lines)

    # ------------------------------------------------------------------
    # Helpers
    # ------------------------------------------------------------------

    def _read_soul_md(self, profile_name: str) -> Optional[str]:
        """Read SOUL.md for a profile, returning None if not found."""
        soul_path = self._config.profiles_dir / profile_name / "SOUL.md"
        if soul_path.is_file():
            try:
                return soul_path.read_text(encoding="utf-8").strip()
            except OSError:
                return None
        return None
